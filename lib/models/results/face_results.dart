import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:intl/intl.dart';

final formatter = DateFormat.yMd();

class FaceResults {
  // PROPERTIES
  final String? faceResultId;
  final String? attachment;
  final String? attachment2;
  final String? attachment3;
  final String? result;
  final String? result2;
  final DateTime? createdAt;

  // CONSTRUCTORS
  FaceResults({
    this.faceResultId,
    this.attachment,
    this.attachment2,
    this.attachment3,
    this.result,
    this.result2,
    this.createdAt,
  });

  factory FaceResults.fromFireStore(
    DocumentSnapshot<Map<String, dynamic>> snapshot,
    //SnapshotOptions? options,
  ) {
    final data = snapshot.data();
    return FaceResults(
      faceResultId: snapshot.id,
      attachment: data?['attachment'],
      attachment2: data?['attachment2'],
      attachment3: data?['attachment3'],
      result: data?['result'],
      result2: data?['result2'],
      createdAt: (data?['createdAt'] as Timestamp?)?.toDate() ?? DateTime.now(),
    );
  }

  // METHODS
  Map<String, dynamic> toFirestore() {
    return {
      // faceResultId auto generated by fb
      'attachment': attachment,
      'attachment2': attachment2,
      'attachment3': attachment3,
      'result': result,
      'result2': result2,
      'createdAt': createdAt ?? DateTime.now(),
    };
  }
}
